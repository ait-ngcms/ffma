package ait.ffma.common.exception;

public class FfmaTechnicalRuntimeException extends RuntimeException {

	/**
	 * This is the base class for all (Ffma) unchecked exceptions.
	 * These exceptions must carry a message and a stack trace that carry enough
	 * information so that the technicians can fix the configuration problems.
	 * 
	 */
	private static final long serialVersionUID = -4489348699086958040L;

	/**
	 * 
	 * @param message
	 * @param th
	 */
	public FfmaTechnicalRuntimeException(String message, Throwable th){
		super(message, th);
	}
	
	/**
	 * 
	 * @param message
	 */
	public FfmaTechnicalRuntimeException(String message){
		super(message);
	}
}
