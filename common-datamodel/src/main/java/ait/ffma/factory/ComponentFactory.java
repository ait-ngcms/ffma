package ait.ffma.factory;

import ait.ffma.domain.FfmaDomainObject;

/**
 * This class defines that interface for the factory method pattern used for instantiating 
 * Ffma domain objects. This class is component specific. 
 * @see CommonFactory
 *  
 * @author GordeaS <gsergiu79@gmx.at>
 *
 */
public interface ComponentFactory {

	/**
	 * This method creates an instance of the domain object identified by the given domain object name
	 * 
	 * @param domainObjectName is the (simple) name of the domain object class
	 * @return an instance of the class identified by the given name 
	 */
	public FfmaDomainObject createDomainObject(String domainObjectName);
	
}
